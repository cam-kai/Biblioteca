    public void modificarCboNombre(){
        DistribuidorDAO distribuidorDao = new DistribuidorDAO();
        ArrayList<Distribuidores> distribuidores = distribuidorDao.listarDistribuidores();
        cboNombreEmpresa.addItem("Seleccione distribuidor");
        
        for (Distribuidores distribuidor : distribuidores) {
            cboNombreEmpresa.addItem(distribuidor.getNombreEmpresa());
        }
    }

