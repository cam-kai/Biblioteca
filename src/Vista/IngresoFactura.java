    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        // TODO add your handling code here:
        if(cboMedioDePago.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un medio de pago");
        }else{
        
            Factura factura = new Factura();
            factura.setPrecioNeto(totalSinIVa);
            factura.setPrecioConIVA(totalIva);
            factura.setCostoConIVA(totalConIva);
            LocalDateTime dateTime = LocalDateTime.now();
            DateTimeFormatter form = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
            String Hoy = String.valueOf(dateTime.format(form));
            String fecha = Hoy.substring(0, 10);
            String hora = Hoy.substring(11, 19);
            factura.setHoraDeCompra(hora);
            factura.setFechaDeCompra(fecha);
            MedioDePago medioP = new MedioDePago();
            int id = cboMedioDePago.getSelectedIndex();
            String medio = String.valueOf(cboMedioDePago.getSelectedItem());
            medioP.setId_medio_de_pago(id);
            medioP.setMedioDePago(medio);
            factura.setMedio_de_pago(medioP);
            factura.setNumeroFactura(Integer.parseInt(txtNumeroFactura.getText()));
            boolean fueAgregadoF =false;
            if(txtNumeroFactura.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Debe ingresar el numero de factura");
            }else{
                fueAgregadoF=new FacturaDAO().fueAgregado(factura);
            }
             

            if(txtIdDistribuidores.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Debe seleccionar un distribuidor");
            }else{
                
                int idDist = Integer.parseInt(txtIdDistribuidores.getText());
                int idFact =  new FacturaDAO().buscarUltimo().getId_factura();
                
                boolean fueAgregadaTCom = new CompraDAO().agregarCompra(idDist, idFact);
                DetalleCompra detalleC = new DetalleCompra();
                int idCom = new CompraDAO().buscarUltimo().getId_compra();
                detalleC.setIdCompra(idCom);
                boolean fueAgregadaRCL = false;
                boolean fueAgregadoStock =false;
                for (ItemCompra itemCompra : productosCompra) {
                    int idLib = itemCompra.getId_libro();
                    detalleC.setIdLibro(idLib);
                    fueAgregadaRCL = new DetalleCompraDAO().agregarDetalleCompra(detalleC);
                    StockLibro stock = new StockLibro();
                    stock.setId_libro(idLib);
                    int stockActual = new StockDAO().bucarPorIdLibro(idLib).getStock_libro();
                    int stockNuevo = stockActual + itemCompra.getCantidad();
                    stock.setStock_libro(stockNuevo);
                    fueAgregadoStock = new StockDAO().modificarStock(stock);
                    
                }
                if(fueAgregadoF== true && fueAgregadaTCom == true && fueAgregadaRCL== true && fueAgregadoStock == true){
                    
                    JOptionPane.showMessageDialog(null, "Fue agregada exitosamente su compra ");
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "No se ha podido realizar su compra");
                }
                
            }
            
        }
    }//GEN-LAST:event_btnComprarActionPerformed

